  required String text,
  Color color = AppColors.primaryText,
  double top = 20,
  double? fontSize,
  FontWeight fontWeight = FontWeight.normal,
}) {
  return Container(
    margin: EdgeInsets.only(top: top),
    child: Text(
      text,
      style: TextStyle(
        color: color,
        fontSize: fontSize,
        fontWeight: fontWeight,
      ),
    ),
  );
}

